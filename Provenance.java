
public class Provenance 
{
	String nom;
	int ID;
	
	public Provenance(String nom, int ID)
	{
		this.nom = nom;
		this.ID = ID;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

}
